            <div id="collapse14" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading14">
                <div class="panel-body">
                    <p>
                        Ce que tout harceleur tente de faire est de gruger, petit à petit, le sentiment de sécurité de sa victime.
                    </p>
                    <p>
                        Mon voisin qui avait fait des menaces en mai 2014 s’est fait arrêter par la police un mardi, à la fin juin. Il a été arrêté et relâché la même journée. La police nous a informé le même jour qu’il avait donné l’engagement qu’il ne communiquerait pas avec nous, soit les Clavet, mon mari et moi, et qu’il obéirait à la condition de ne pas être présent à son chalet après 20h00 les soirs et les fins de semaines, jusqu’à ordonnance au contraire. La police prendrait aussi rendez-vous avec lui le lendemain pour qu’il remette toute arme à feu en sa possession. On nous a informés de ces conditions, et on nous a demandé d’appeler la police en cas de violation.
                    </p>
                    <p>
                        Ce soir-là, le voisin est retourné à son chalet. La voisine, visiblement en colère, était assise sur son perron et épiait dans la maison des Clavet avec des jumelles. Le voisin n’est pas reparti à l’heure convenue. Nous ne savions pas quelle était l’intention.
                    </p>
                    <p>
                        Nous avons donc appelé la police, tel que nous en avions reçu l’instruction. Notre appel a reçu la plus haute priorité, la police est arrivée aussi rapidement qu’elle a pu. Ils ont trouvé le voisin dans son chalet, mais il refusait d’en sortir pour être mis sous arrestation. Sans mandat d’arrêt, impossible de le prendre en détention. Mais la police n’a pas les ressources pour qu’un membre reste toute la nuit pour assurer la paix.
                    </p>
                    <p>
                        Avant de repartir, la police a indiqué à Pierre qu’il n’y avait pas lieu de s’inquiéter : le voisin avait donné sa parole qu’il ne ferait rien de sinistre. Nous étions censés nous fier à la parole du voisin, accusé d’avoir proféré la menace « you’re gonna f---ing die », à qui la police n’avait pas encore enlevé ses armes à feu, alors qu’il était en violation de ses conditions.  Nous étions terrifiés.
                    </p>
                    <p>
                        Lorsque le voisin a comparu devant un juge après avoir été éventuellement remis en état d’arrestation, ses conditions ont été réduites avant qu’il soit relâché. Ses conditions lui permettent désormais d’être au chalet en tout temps, afin qu’il puisse prendre avantage de «son droit de profiter de sa propriété», du moins c’est ce qu’on nous a expliqué sans même remarquer l’ironie de la chose. Il a un couvre-feu, sur lequel on nous donne la responsabilité de veiller en appelant la police si le voisin ne le respecte pas. Nous ne nous en préoccupons pas.
                    </p>
                    <p>
                        Ce que tout harceleur tente de faire est de gruger, petit à petit, le sentiment de sécurité de sa victime. Les victimes d’harcèlement souffrent d’être en état d’alerte constant, même lorsque le harceleur ne harcèle pas. Le mot pour décrire cet état est hypervigilance.
                    </p>
                    <p>
                        Ce qui est dommage, c’est que même si le système juridique est très limité relativement à l’aide qu’elle peut fournir à une victime de harcèlement, ce que le système exige de la victime a pour effet d’accentuer son angoisse.
